export const getPackageById = (id) => {
  return packages.find(pkg => pkg.id === id);
};

export const getPackagesByCategory = (category) => {
  if (category === 'all') return packages;
  return packages.filter(pkg => pkg.category.toLowerCase() === category.toLowerCase());
};

export const getFeaturedPackages = () => {
  return packages.filter(pkg => pkg.featured);
};

export const getPopularPackages = () => {
  return packages.filter(pkg => pkg.popular);
};
