export const getInventoryStats = () => {
  return {
    totalItems: completeInventory.length,
    lowStockItems: completeInventory.filter(item => item.qohAllBatches > 0 && item.qohAllBatches <= 20).length,
    criticalItems: completeInventory.filter(item => item.qohAllBatches === 0 || (item.reorderLevel > 0 && item.qohAllBatches <= item.reorderLevel)).length,
    totalValue: completeInventory.reduce((sum, item) => sum + (item.inventoryAfterTaxSellingPrice * item.qohAllBatches), 0)
  };
};

export const getInventoryById = (id) => completeInventory.find(item => item.id === id);
export const getInventoryByCategory = (category) => {
  if (category === 'all') return completeInventory;
  return completeInventory.filter(item => item.inventoryCategory.toLowerCase().includes(category));
};
